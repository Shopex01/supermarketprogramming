package Objects.Goods;

/**
 * Die Enum "GoodPropertyEnumerations" stellt 4 (weitere) Eigenschaften für die weitere Unterscheidung von Produkten zur Verfügung!
 * @author Arthur Ladner
 * @version 1.0
 */
public enum GoodPropertyEnumerations {
    /**
     * "Keine" Eigenschaft
     */
    NONE,
    /**
     * Eigenschaft "Mindesthaltbarkeitsdatum"
     */
    EXPIRATIONDATE,
    /**
     * Eigenschaft "Recycling-Anteil"
     */
    RECYCLING,
    /**
     * Eigenschaft "FSK-Kategorie"
     */
    FSK
}
